package com.xiaotu.advertiser.implant.model.map;

import com.xiaotu.advertiser.implant.model.ImplantRecordModel;
import com.xiaotu.advertiser.project.model.PlayRoleModel;

/**
 * 广告植入记录与角色关联关系
 * @author xuchangjian 2017-6-19下午4:58:41
 */
public class ImplantRoleMapModel {

	private String id;
	private ImplantRecordModel implantRecord;	//广告植入记录
	private PlayRoleModel playRole;	//角色
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public ImplantRecordModel getImplantRecord() {
		return implantRecord;
	}
	public void setImplantRecord(ImplantRecordModel implantRecord) {
		this.implantRecord = implantRecord;
	}
	public PlayRoleModel getPlayRole() {
		return playRole;
	}
	public void setPlayRole(PlayRoleModel playRole) {
		this.playRole = playRole;
	}
}
