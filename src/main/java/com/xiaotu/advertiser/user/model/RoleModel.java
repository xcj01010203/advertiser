package com.xiaotu.advertiser.user.model;

import java.util.Date;

import org.springframework.beans.factory.annotation.Autowired;

/**
 * @类名 RoleModel
 * @日期 2017年6月19日
 * @作者   王艳龙
 * @功能   角色实体类
 */
public class RoleModel implements java.io.Serializable {

	private static final long serialVersionUID = 1L;

	private String id;//主键标识
	private String roleName;// 角色名称
	private String roleExplain;// 角色描述
	private String creator;// 创建者
	private Date createTime;// 创建时间
	private String lastEditor;// 最后修改人
	private Date lastEditTime;// 最后修改时间

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}

	public String getLastEditor() {
		return lastEditor;
	}

	public void setLastEditor(String lastEditor) {
		this.lastEditor = lastEditor;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getLastEditTime() {
		return lastEditTime;
	}

	public void setLastEditTime(Date lastEditTime) {
		this.lastEditTime = lastEditTime;
	}

	public String getRoleExplain() {
		return roleExplain;
	}

	public void setRoleExplain(String roleExplain) {
		this.roleExplain = roleExplain;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

}
