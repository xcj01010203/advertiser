package com.xiaotu.advertiser.user.model;

/**
 * @类名 MenuModel
 * @日期 2017年6月19日
 * @作者 王艳龙
 * @功能 菜单实体类
 */
public class MenuModel implements java.io.Serializable {

	private static final long serialVersionUID = 1L;

	private String id;// uuid
	private String menuId;// 菜单ID
	private String menuName;// 菜单名称
	private String fid;// 菜单父ID
	private Integer menuLv;// 菜单等级
	private String menuExplain;// 菜单描述
	private String menuURL;// 菜单访问路径
	private String cssName;// 菜单图表样式

	public String getCssName() {
		return cssName;
	}

	public void setCssName(String cssName) {
		this.cssName = cssName;
	}

	public Integer getMenuLv() {
		return menuLv;
	}

	public void setMenuLv(Integer menuLv) {
		this.menuLv = menuLv;
	}

	public String getFid() {
		return fid;
	}

	public void setFid(String fid) {
		this.fid = fid;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMenuId() {
		return menuId;
	}

	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public String getMenuExplain() {
		return menuExplain;
	}

	public void setMenuExplain(String menuExplain) {
		this.menuExplain = menuExplain;
	}

	public String getMenuURL() {
		return menuURL;
	}

	public void setMenuURL(String menuURL) {
		this.menuURL = menuURL;
	}

}
