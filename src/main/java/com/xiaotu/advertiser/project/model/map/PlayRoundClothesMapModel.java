package com.xiaotu.advertiser.project.model.map;

import com.xiaotu.advertiser.project.model.ClothesModel;
import com.xiaotu.advertiser.project.model.PlayRoundModel;

/**
 * 场次和服装的关联关系
 * @author xuchangjian 2017年6月22日下午3:32:31
 */
public class PlayRoundClothesMapModel {

	private String id;
	private PlayRoundModel playRound;	//场次信息
	private ClothesModel clothes;	//服装信息
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public PlayRoundModel getPlayRound() {
		return playRound;
	}
	public void setPlayRound(PlayRoundModel playRound) {
		this.playRound = playRound;
	}
	public ClothesModel getClothes() {
		return clothes;
	}
	public void setClothes(ClothesModel clothes) {
		this.clothes = clothes;
	}
}
