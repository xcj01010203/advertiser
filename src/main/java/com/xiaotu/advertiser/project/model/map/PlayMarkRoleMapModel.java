package com.xiaotu.advertiser.project.model.map;

import com.xiaotu.advertiser.project.model.PlayMarkModel;
import com.xiaotu.advertiser.project.model.PlayRoleModel;

/**
 * 场景角色与剧本标记关联表
 * @author xuchangjian 2017-6-19下午5:10:04
 */
public class PlayMarkRoleMapModel {

	private String id;
	private PlayRoleModel playRole;	//角色
	private PlayMarkModel playMark;	//标记
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public PlayRoleModel getPlayRole() {
		return playRole;
	}
	public void setPlayRole(PlayRoleModel playRole) {
		this.playRole = playRole;
	}
	public PlayMarkModel getPlayMark() {
		return playMark;
	}
	public void setPlayMark(PlayMarkModel playMark) {
		this.playMark = playMark;
	}
}
