package com.xiaotu.advertiser.project.model;

/**
 * 需要提示替换或跳过的场次数据
 * @author xuchangjian 2017年6月21日下午5:30:38
 */
public class PlayRoundTmpModel {

	private String id;
	private ProjectModel project;	//项目信息
	private PlayRoundModel playRound;	//场次信息
	private int seriesNo;	//集次
	private String roundNo;	//场次
	private String atmosphere;	//气氛
	private String site;	//内外景
	private String title;	//标题
	private String content;	//剧本内容
	private String majorRoleNames;	//主要角色名称，多个用逗号隔开
	private String firstLocation;	//一级场景
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public ProjectModel getProject() {
		return project;
	}
	public void setProject(ProjectModel project) {
		this.project = project;
	}
	public PlayRoundModel getPlayRound() {
		return playRound;
	}
	public void setPlayRound(PlayRoundModel playRound) {
		this.playRound = playRound;
	}
	public int getSeriesNo() {
		return seriesNo;
	}
	public void setSeriesNo(int seriesNo) {
		this.seriesNo = seriesNo;
	}
	public String getRoundNo() {
		return roundNo;
	}
	public void setRoundNo(String roundNo) {
		this.roundNo = roundNo;
	}
	public String getAtmosphere() {
		return atmosphere;
	}
	public void setAtmosphere(String atmosphere) {
		this.atmosphere = atmosphere;
	}
	public String getSite() {
		return site;
	}
	public void setSite(String site) {
		this.site = site;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getMajorRoleNames() {
		return majorRoleNames;
	}
	public void setMajorRoleNames(String majorRoleNames) {
		this.majorRoleNames = majorRoleNames;
	}
	public String getFirstLocation() {
		return firstLocation;
	}
	public void setFirstLocation(String firstLocation) {
		this.firstLocation = firstLocation;
	}
	@Override
	public boolean equals(Object obj) {
		PlayRoundTmpModel tmp = (PlayRoundTmpModel) obj;
		return tmp.getPlayRound().getId().equals(this.playRound.getId());
	}
}
